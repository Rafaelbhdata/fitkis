// Función para obtener preguntas aleatorias que no se han usado
export function getRandomQuestions(
  usedQuestionIndices: number[],
  count: number = 3
): { index: number; question: string }[] {
  const availableIndices = JOURNAL_QUESTIONS
    .map((_, index) => index)
    .filter(index => !usedQuestionIndices.includes(index))

  // Si no hay suficientes preguntas disponibles, reiniciar el banco
  if (availableIndices.length < count) {
    // Retornar las que quedan + reiniciar
    const remaining = availableIndices.map(index => ({
      index,
      question: JOURNAL_QUESTIONS[index]
    }))

    // Si necesitamos más, tomar del inicio del banco
    const needed = count - remaining.length
    const fromStart = Array.from({ length: needed }, (_, i) => ({
      index: i,
      question: JOURNAL_QUESTIONS[i]
    }))

    return [...remaining, ...fromStart]
  }

  // Seleccionar aleatoriamente
  const selected: { index: number; question: string }[] = []
  const tempAvailable = [...availableIndices]

  for (let i = 0; i < count; i++) {
    const randomIdx = Math.floor(Math.random() * tempAvailable.length)
    const questionIndex = tempAvailable[randomIdx]
    selected.push({
      index: questionIndex,
      question: JOURNAL_QUESTIONS[questionIndex]
    })
    tempAvailable.splice(randomIdx, 1)
  }

  return selected
}

// Función para obtener una pregunta de reemplazo
export function getReplacementQuestion(
  usedQuestionIndices: number[],
  currentQuestionIndices: number[]
): { index: number; question: string } | null {
  const excludeIndices = [...usedQuestionIndices, ...currentQuestionIndices]
  const availableIndices = JOURNAL_QUESTIONS
    .map((_, index) => index)
    .filter(index => !excludeIndices.includes(index))

  if (availableIndices.length === 0) {
    return null
  }

  const randomIdx = Math.floor(Math.random() * availableIndices.length)
  const questionIndex = availableIndices[randomIdx]

  return {
    index: questionIndex,
    question: JOURNAL_QUESTIONS[questionIndex]
  }
}
